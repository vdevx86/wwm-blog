namespace Wwm\Blog\Cms\WordPress\Bootstrap;

trait FileFactoryTrait
{
    
    public function create(array $data = [])
    {
        $data = array_merge($this->data, $data);
        return $this->objectManager->create($this->instanceName, $data);
    }
    
}
